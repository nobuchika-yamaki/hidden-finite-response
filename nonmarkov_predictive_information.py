# Linear-Gaussian analytic theory
# ============================================================

def expm_2x2(A: np.ndarray, t: float) -> np.ndarray:
    vals, vecs = np.linalg.eig(A)
    E = vecs @ np.diag(np.exp(vals * t)) @ np.linalg.inv(vecs)
    return np.real_if_close(E).real


def solve_ou_covariance(A: np.ndarray, Q: np.ndarray) -> np.ndarray:
    # Solve A Sigma + Sigma A^T + Q = 0.
    n = A.shape[0]
    K = np.kron(np.eye(n), A) + np.kron(A, np.eye(n))
    b = -Q.reshape(-1)
    Sigma = np.linalg.solve(K, b).reshape(n, n)
    return 0.5 * (Sigma + Sigma.T)


def linear_covariance_x(c: Config, h: float) -> float:
    A = np.array([
        [-c.lin_alpha, c.lin_beta],
        [ c.lin_kappa, -c.lin_gamma],
    ], dtype=float)
    Q = np.diag([c.lin_sigma_x ** 2, c.lin_sigma_y ** 2])
    Sigma = solve_ou_covariance(A, Q)
    E = expm_2x2(A.T, abs(h))
    C = Sigma @ E
    return float(C[0, 0])


def gaussian_conditional_te_from_cov(C0: float, Cl: float, Cd: float, Cld: float) -> float:
    # TE = I(X_{t-l}; X_{t+d} | X_t)
    var_past_cond = C0 - (Cl * Cl) / C0
    var_fut_cond = C0 - (Cd * Cd) / C0
    cov_cond = Cld - (Cl * Cd) / C0
    denom = math.sqrt(max(var_past_cond * var_fut_cond, 1e-300))
    rho = cov_cond / denom
    rho = max(min(rho, 0.999999999), -0.999999999)
    return float(-0.5 * math.log(1.0 - rho * rho))


def run_linear_theory(c: Config, tables: Path) -> pd.DataFrame:
    tau = 1.0 / c.lin_gamma
    rows = []
    for lr in c.lag_ratios:
        Delta = lr * tau
        past = tau
        C0 = linear_covariance_x(c, 0.0)
        Cl = linear_covariance_x(c, past)
        Cd = linear_covariance_x(c, Delta)
        Cld = linear_covariance_x(c, past + Delta)
        te = gaussian_conditional_te_from_cov(C0, Cl, Cd, Cld)
        rows.append({
            "lag_ratio": lr,
            "DeltaT": Delta,
            "past_lag": past,
            "TE_gaussian_analytic": te,
        })
    df = pd.DataFrame(rows)
    df.to_csv(tables / "linear_gaussian_theory_TE.csv", index=False)
    return df


