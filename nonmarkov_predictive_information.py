def run_local_parameter_robustness(c: Config, tables: Path):
    rng_master = np.random.default_rng(c.seed + 3000)
    tau_ref = 1.0 / c.gamma
    rows = []

    perturbations = []
    for param in ["beta", "kappa", "gamma"]:
        for factor in c.robustness_factors:
            perturbations.append((param, factor))

    for param, factor in perturbations:
        for run in range(c.robustness_runs):
            rng = np.random.default_rng(rng_master.integers(0, 2**32 - 1))
            kwargs = {}
            if param == "beta":
                kwargs["beta"] = c.beta * factor
            if param == "kappa":
                kwargs["kappa"] = c.kappa * factor
            if param == "gamma":
                kwargs["gamma"] = c.gamma * factor
            x, y, _ = simulate_finite_response(c, rng, **kwargs)
            rec = metrics_for_x(x, c, c.N_primary, 1.0, tau_ref)
            rec.update({"run": run, "parameter": param, "factor": factor})
            rows.append(rec)

    df = pd.DataFrame(rows)
    df.to_csv(tables / "local_parameter_robustness_raw.csv", index=False)
    summ = summarize(df, ["parameter", "factor"], ["TE", "M_nonMarkov", "stationarity_distance"])
    summ.to_csv(tables / "local_parameter_robustness_summary.csv", index=False)
    return df, summ


