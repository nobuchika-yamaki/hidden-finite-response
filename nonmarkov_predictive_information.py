def transition_counts(S: np.ndarray, lag_steps: int, N: int) -> Tuple[np.ndarray, np.ndarray]:
    lag_steps = max(1, int(lag_steps))
    src = S[:-lag_steps]
    dst = S[lag_steps:]
    counts = np.zeros((N, N), dtype=np.float64)
    np.add.at(counts, (src, dst), 1.0)
    occ = np.bincount(src, minlength=N).astype(np.float64)
    return counts, occ


def counts_to_P(counts: np.ndarray, pseudocount: float) -> np.ndarray:
    N = counts.shape[0]
    C = counts.astype(np.float64).copy()
    if pseudocount > 0:
        C += pseudocount
    row = C.sum(axis=1, keepdims=True)
    P = np.divide(C, row, out=np.ones_like(C) / N, where=row > 0)
    return P


def nonmarkov_measure(S: np.ndarray, lag_steps: int, N: int, pseudocount: float) -> float:
    counts1, _ = transition_counts(S, lag_steps, N)
    P1 = counts_to_P(counts1, pseudocount)
    counts2, _ = transition_counts(S, 2 * lag_steps, N)
    P2 = counts_to_P(counts2, pseudocount)
    pred = P1 @ P1
    den = np.linalg.norm(P2, ord="fro")
    if den <= 1e-15:
        return np.nan
    return float(np.linalg.norm(P2 - pred, ord="fro") / den)


def make_te_arrays(S: np.ndarray, future_steps: int, past_steps: int) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    future_steps = max(1, future_steps)
    past_steps = max(1, past_steps)
    start = past_steps
    stop = len(S) - future_steps
    if stop <= start + 10:
        return np.array([], dtype=int), np.array([], dtype=int), np.array([], dtype=int)
    past = S[start - past_steps:stop - past_steps]
    curr = S[start:stop]
    fut = S[start + future_steps:stop + future_steps]
    return fut.astype(np.int64), curr.astype(np.int64), past.astype(np.int64)


def transfer_entropy_from_arrays(fut: np.ndarray, curr: np.ndarray, past: np.ndarray, N: int, pseudocount: float) -> float:
    if len(fut) == 0:
        return np.nan

    idx3 = fut * (N * N) + curr * N + past
    c_fcp = np.bincount(idx3, minlength=N * N * N).reshape(N, N, N).astype(np.float64)

    c_cp = np.sum(c_fcp, axis=0)  # curr, past
    c_fc = np.sum(c_fcp, axis=2)  # fut, curr
    c_c = np.sum(c_cp, axis=1)    # curr

    total = float(len(fut))
    floor = 1e-300
    te = 0.0

    nz = np.argwhere(c_fcp > 0)
    pc = pseudocount

    for f, cc, p in nz:
        n = c_fcp[f, cc, p]
        p_joint = n / total

        den_cp = c_cp[cc, p] + pc * N
        den_c = c_c[cc] + pc * N
        if den_cp <= 0 or den_c <= 0:
            continue

        p_f_given_cp = max(float((n + pc) / den_cp), floor)
        p_f_given_c = max(float((c_fc[f, cc] + pc) / den_c), floor)

        te += p_joint * math.log(p_f_given_cp / p_f_given_c)

    return float(te)


def transfer_entropy(S: np.ndarray, future_steps: int, past_steps: int, N: int, pseudocount: float) -> float:
    fut, curr, past = make_te_arrays(S, future_steps, past_steps)
    return transfer_entropy_from_arrays(fut, curr, past, N, pseudocount)


def shifted_past_null_TE(S: np.ndarray, future_steps: int, past_steps: int, N: int, pseudocount: float,
                         rng: np.random.Generator, n_null: int) -> np.ndarray:
    fut, curr, past = make_te_arrays(S, future_steps, past_steps)
    if len(fut) == 0:
        return np.full(n_null, np.nan)

    vals = []
    L = len(past)
    for _ in range(n_null):
        shift = int(rng.integers(max(1, L // 10), max(2, L - L // 10)))
        past_shifted = np.roll(past, shift)
        vals.append(transfer_entropy_from_arrays(fut, curr, past_shifted, N, pseudocount))
    return np.asarray(vals, dtype=np.float64)


def permuted_past_null_TE(S: np.ndarray, future_steps: int, past_steps: int, N: int, pseudocount: float,
                          rng: np.random.Generator, n_null: int) -> np.ndarray:
    fut, curr, past = make_te_arrays(S, future_steps, past_steps)
    if len(fut) == 0:
        return np.full(n_null, np.nan)

    vals = []
    for _ in range(n_null):
        vals.append(transfer_entropy_from_arrays(fut, curr, rng.permutation(past), N, pseudocount))
    return np.asarray(vals, dtype=np.float64)


def sequence_permutation_null(S: np.ndarray, future_steps: int, past_steps: int, N: int, pseudocount: float,
                              rng: np.random.Generator, n_null: int) -> Tuple[np.ndarray, np.ndarray]:
    vals_te = []
    vals_nm = []
    for _ in range(n_null):
        Sp = rng.permutation(S)
        vals_te.append(transfer_entropy(Sp, future_steps, past_steps, N, pseudocount))
        vals_nm.append(nonmarkov_measure(Sp, future_steps, N, pseudocount))
    return np.asarray(vals_te), np.asarray(vals_nm)


def stationarity_distance(S: np.ndarray, lag_steps: int, N: int, pseudocount: float) -> float:
    mid = len(S) // 2
    if mid <= 2 * lag_steps:
        return np.nan
    c1, o1 = transition_counts(S[:mid], lag_steps, N)
    c2, o2 = transition_counts(S[mid:], lag_steps, N)
    P1 = counts_to_P(c1, pseudocount)
    P2 = counts_to_P(c2, pseudocount)
    occ1 = o1 / max(np.sum(o1), 1.0)
    occ2 = o2 / max(np.sum(o2), 1.0)
    return float(np.linalg.norm(P1 - P2, ord="fro") + np.linalg.norm(occ1 - occ2))


def metrics_for_x(
    x: np.ndarray,
    c: Config,
    N: int,
    lag_ratio: float,
    tau_ref: float,
    partition: str = "quantile",
    past_ratio: float = 1.0,
) -> Dict[str, float]:
    future_steps = max(1, int(round(lag_ratio * tau_ref / c.dt)))
    past_steps = max(1, int(round(past_ratio * tau_ref / c.dt)))

    edges = get_edges(x, N, partition, future_steps, c)
    S = digitize(x, edges)

    counts, _ = transition_counts(S, future_steps, N)
    min_out = float(np.min(counts.sum(axis=1))) if counts.size else 0.0
    ok = min_out >= c.min_outgoing_count

    te = transfer_entropy(S, future_steps, past_steps, N, c.pseudocount) if ok else np.nan
    nm = nonmarkov_measure(S, future_steps, N, c.pseudocount) if ok else np.nan
    stat = stationarity_distance(S, future_steps, N, c.pseudocount)

    return {
        "N": N,
        "lag_ratio": lag_ratio,
        "past_ratio": past_ratio,
        "future_steps": future_steps,
        "past_steps": past_steps,
        "partition": partition,
        "min_outgoing_count": min_out,
        "min_count_ok": ok,
        "TE": te,
        "M_nonMarkov": nm,
        "stationarity_distance": stat,
    }


def sem(vals: np.ndarray) -> float:
    vals = np.asarray(vals, dtype=np.float64)
    vals = vals[np.isfinite(vals)]
    if len(vals) <= 1:
        return np.nan
    return float(np.std(vals, ddof=1) / math.sqrt(len(vals)))


def summarize(df: pd.DataFrame, group_cols: List[str], metrics: List[str]) -> pd.DataFrame:
    rows = []
    for key, g in df.groupby(group_cols):
        if not isinstance(key, tuple):
            key = (key,)
        row = {col: val for col, val in zip(group_cols, key)}
        row["n"] = len(g)
        for m in metrics:
            vals = pd.to_numeric(g[m], errors="coerce").to_numpy(dtype=np.float64)
            vals = vals[np.isfinite(vals)]
            row[f"{m}_mean"] = float(np.mean(vals)) if len(vals) else np.nan
            row[f"{m}_sem"] = sem(vals) if len(vals) else np.nan
            row[f"{m}_median"] = float(np.median(vals)) if len(vals) else np.nan
        rows.append(row)
    return pd.DataFrame(rows)


