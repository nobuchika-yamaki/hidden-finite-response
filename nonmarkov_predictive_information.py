def quantile_edges(x: np.ndarray, N: int) -> np.ndarray:
    edges = np.quantile(x, np.linspace(0.0, 1.0, N + 1))
    edges = np.asarray(edges, dtype=np.float64)
    for i in range(1, len(edges)):
        if edges[i] <= edges[i-1]:
            edges[i] = edges[i-1] + 1e-10
    edges[0] -= 1e-9
    edges[-1] += 1e-9
    return edges


def equal_width_edges(x: np.ndarray, N: int) -> np.ndarray:
    mn = float(np.min(x))
    mx = float(np.max(x))
    if mx <= mn:
        mx = mn + 1e-9
    edges = np.linspace(mn, mx, N + 1)
    edges[0] -= 1e-9
    edges[-1] += 1e-9
    return edges


def digitize(x: np.ndarray, edges: np.ndarray) -> np.ndarray:
    S = np.digitize(x, edges[1:-1], right=False)
    S[S < 0] = 0
    S[S >= len(edges) - 1] = len(edges) - 2
    return S.astype(np.int64)


def mutual_information_symbols(a: np.ndarray, b: np.ndarray, N: int, pc: float = 1e-12) -> float:
    counts = np.zeros((N, N), dtype=np.float64)
    np.add.at(counts, (a, b), 1.0)
    counts += pc
    pxy = counts / np.sum(counts)
    px = np.sum(pxy, axis=1, keepdims=True)
    py = np.sum(pxy, axis=0, keepdims=True)
    return float(np.sum(pxy * np.log(pxy / (px @ py))))


def mi_edges(x: np.ndarray, N: int, lag_steps: int, c: Config) -> np.ndarray:
    # Greedy partition used only as a robustness check.
    if len(x) > c.mi_subsample:
        idx = np.linspace(0, len(x) - 1, c.mi_subsample).astype(int)
        xs = x[idx]
    else:
        xs = x

    if len(xs) <= lag_steps + 10:
        return quantile_edges(x, N)

    x0 = xs[:-lag_steps]
    x1 = xs[lag_steps:]
    candidates = np.unique(np.quantile(xs, np.linspace(0.02, 0.98, c.mi_candidate_count)))
    thresholds: List[float] = []
    mn = float(np.min(x)) - 1e-9
    mx = float(np.max(x)) + 1e-9

    for _ in range(N - 1):
        best_thr = None
        best_mi = -np.inf
        for thr in candidates:
            if any(abs(float(thr) - t) < 1e-12 for t in thresholds):
                continue
            trial = sorted(thresholds + [float(thr)])
            edges = np.array([mn] + trial + [mx], dtype=np.float64)
            nb = len(edges) - 1
            s0 = digitize(x0, edges)
            s1 = digitize(x1, edges)
            mi = mutual_information_symbols(s0, s1, nb)
            if mi > best_mi:
                best_mi = mi
                best_thr = float(thr)
        if best_thr is None:
            return quantile_edges(x, N)
        thresholds.append(best_thr)

    edges = np.array([mn] + sorted(thresholds) + [mx], dtype=np.float64)
    if len(edges) != N + 1:
        return quantile_edges(x, N)
    return edges


def get_edges(x: np.ndarray, N: int, method: str, lag_steps: int, c: Config) -> np.ndarray:
    if method == "quantile":
        return quantile_edges(x, N)
    if method == "equal_width":
        return equal_width_edges(x, N)
    if method == "mi":
        return mi_edges(x, N, lag_steps, c)
    raise ValueError(method)


