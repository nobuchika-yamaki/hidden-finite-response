def run_null_models(c: Config, runs: List[Dict[str, object]], tables: Path):
    rng = np.random.default_rng(c.seed + 2000)
    tau_ref = 1.0 / c.gamma
    future_steps = max(1, int(round(tau_ref / c.dt)))
    past_steps = future_steps
    N = c.N_primary

    x = runs[0]["x"]
    edges = quantile_edges(x, N)
    S = digitize(x, edges)

    observed_TE = transfer_entropy(S, future_steps, past_steps, N, c.pseudocount)
    observed_M = nonmarkov_measure(S, future_steps, N, c.pseudocount)

    perm_TE, perm_M = sequence_permutation_null(S, future_steps, past_steps, N, c.pseudocount, rng, c.n_null)
    shifted_TE = shifted_past_null_TE(S, future_steps, past_steps, N, c.pseudocount, rng, c.n_null)
    pastperm_TE = permuted_past_null_TE(S, future_steps, past_steps, N, c.pseudocount, rng, c.n_null)

    rows = []
    for name, metric, obs, vals in [
        ("sequence_permutation", "TE", observed_TE, perm_TE),
        ("sequence_permutation", "M_nonMarkov", observed_M, perm_M),
        ("shifted_past", "TE", observed_TE, shifted_TE),
        ("permuted_past", "TE", observed_TE, pastperm_TE),
    ]:
        vals = np.asarray(vals, dtype=np.float64)
        vals = vals[np.isfinite(vals)]
        rows.append({
            "null": name,
            "metric": metric,
            "observed": obs,
            "null_mean": float(np.mean(vals)) if len(vals) else np.nan,
            "null_ci_low": float(np.percentile(vals, 2.5)) if len(vals) else np.nan,
            "null_ci_high": float(np.percentile(vals, 97.5)) if len(vals) else np.nan,
            "p_value": float(np.mean(vals >= obs)) if len(vals) else np.nan,
        })

    raw_rows = []
    for i, v in enumerate(perm_TE):
        raw_rows.append({"null": "sequence_permutation", "metric": "TE", "iter": i, "value": v})
    for i, v in enumerate(perm_M):
        raw_rows.append({"null": "sequence_permutation", "metric": "M_nonMarkov", "iter": i, "value": v})
    for i, v in enumerate(shifted_TE):
        raw_rows.append({"null": "shifted_past", "metric": "TE", "iter": i, "value": v})
    for i, v in enumerate(pastperm_TE):
        raw_rows.append({"null": "permuted_past", "metric": "TE", "iter": i, "value": v})

    raw = pd.DataFrame(raw_rows)
    raw.to_csv(tables / "null_models_raw.csv", index=False)
    summ = pd.DataFrame(rows)
    summ.to_csv(tables / "null_models_summary.csv", index=False)
    return raw, summ


