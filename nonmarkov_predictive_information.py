def compute_steps(c: Config, gamma: float) -> Tuple[int, int, float]:
    tau = 1.0 / gamma
    burn = max(c.burn_min, c.burn_tau_multiplier * tau)
    steps = int(round(c.T / c.dt))
    burn_steps = int(round(burn / c.dt))
    if burn_steps >= steps // 2:
        steps = burn_steps + int(round(max(c.T, 20.0 * tau) / c.dt))
    return steps, burn_steps, tau


def simulate_finite_response(
    c: Config,
    rng: np.random.Generator,
    alpha: Optional[float] = None,
    beta: Optional[float] = None,
    gamma: Optional[float] = None,
    kappa: Optional[float] = None,
    sigma_x: Optional[float] = None,
    sigma_y: Optional[float] = None,
    response: str = "tanh",
    ou_noise: bool = False,
) -> Tuple[np.ndarray, np.ndarray, float]:
    alpha = c.alpha if alpha is None else alpha
    beta = c.beta if beta is None else beta
    gamma = c.gamma if gamma is None else gamma
    kappa = c.kappa if kappa is None else kappa
    sigma_x = c.sigma_x if sigma_x is None else sigma_x
    sigma_y = c.sigma_y if sigma_y is None else sigma_y

    steps, burn_steps, tau = compute_steps(c, gamma)
    x = np.zeros(steps, dtype=np.float64)
    y = np.zeros(steps, dtype=np.float64)
    sqrt_dt = math.sqrt(c.dt)

    if response == "tanh":
        def f(v): return math.tanh(v)
    elif response == "linear":
        def f(v): return v
    else:
        raise ValueError(response)

    if ou_noise:
        rho = math.exp(-c.dt / c.ou_corr_time)
        s = math.sqrt(max(0.0, 1.0 - rho * rho))
        nx = 0.0
        ny = 0.0
        for n in range(steps - 1):
            nx = rho * nx + s * rng.normal()
            ny = rho * ny + s * rng.normal()
            x[n+1] = x[n] + (-alpha * x[n] + beta * f(y[n])) * c.dt + sigma_x * sqrt_dt * nx
            y[n+1] = y[n] + (-gamma * y[n] + kappa * x[n]) * c.dt + sigma_y * sqrt_dt * ny
    else:
        for n in range(steps - 1):
            x[n+1] = x[n] + (-alpha * x[n] + beta * f(y[n])) * c.dt + sigma_x * sqrt_dt * rng.normal()
            y[n+1] = y[n] + (-gamma * y[n] + kappa * x[n]) * c.dt + sigma_y * sqrt_dt * rng.normal()

    return x[burn_steps:], y[burn_steps:], tau


def simulate_equilibrium_control(c: Config, rng: np.random.Generator) -> Tuple[np.ndarray, np.ndarray, float]:
    # Stable detailed-balance linear gradient-flow control.
    a = c.alpha
    b = c.gamma
    coupling = 0.45 * math.sqrt(a * b)
    temp = max(1e-8, c.sigma_x * c.sigma_x / 2.0)

    steps, burn_steps, tau = compute_steps(c, c.gamma)
    x = np.zeros(steps, dtype=np.float64)
    y = np.zeros(steps, dtype=np.float64)
    sqrt_2T_dt = math.sqrt(2.0 * temp * c.dt)

    for n in range(steps - 1):
        x[n+1] = x[n] + (-a * x[n] + coupling * y[n]) * c.dt + sqrt_2T_dt * rng.normal()
        y[n+1] = y[n] + ( coupling * x[n] - b * y[n]) * c.dt + sqrt_2T_dt * rng.normal()

    return x[burn_steps:], y[burn_steps:], tau


