def generate_main_runs(c: Config) -> List[Dict[str, object]]:
    rng_master = np.random.default_rng(c.seed)
    runs = []
    for r in range(c.n_runs):
        rng = np.random.default_rng(rng_master.integers(0, 2**32 - 1))
        x, y, tau = simulate_finite_response(c, rng)
        runs.append({"run": r, "x": x, "y": y, "tau": tau})
    return runs


def run_lag_analysis(c: Config, runs: List[Dict[str, object]], tables: Path):
    tau_ref = 1.0 / c.gamma
    rows = []
    for item in runs:
        for lr in c.lag_ratios:
            rec = metrics_for_x(item["x"], c, c.N_primary, lr, tau_ref)
            rec.update({"run": item["run"]})
            rows.append(rec)
    df = pd.DataFrame(rows)
    df.to_csv(tables / "main_lag_raw.csv", index=False)
    summ = summarize(df, ["lag_ratio"], ["TE", "M_nonMarkov", "stationarity_distance"])
    summ.to_csv(tables / "main_lag_summary.csv", index=False)
    return df, summ


def run_N_lag_analysis(c: Config, runs: List[Dict[str, object]], tables: Path):
    tau_ref = 1.0 / c.gamma
    rows = []
    for item in runs:
        for N in c.N_values:
            for lr in c.lag_ratios:
                rec = metrics_for_x(item["x"], c, N, lr, tau_ref)
                rec.update({"run": item["run"]})
                rows.append(rec)
    df = pd.DataFrame(rows)
    df.to_csv(tables / "N_lag_raw.csv", index=False)
    summ = summarize(df, ["N", "lag_ratio"], ["TE", "M_nonMarkov", "stationarity_distance"])
    summ.to_csv(tables / "N_lag_summary.csv", index=False)

    opt_rows = []
    for metric in ["TE", "M_nonMarkov"]:
        for (run, N), g in df.groupby(["run", "N"]):
            gg = g[np.isfinite(g[metric])]
            if len(gg) == 0:
                continue
            idx = gg[metric].idxmax()
            opt_rows.append({
                "run": run,
                "N": N,
                "metric": metric,
                "optimal_lag_ratio": float(gg.loc[idx, "lag_ratio"]),
                "peak_value": float(gg.loc[idx, metric]),
            })
    opt = pd.DataFrame(opt_rows)
    opt.to_csv(tables / "optimal_lag_by_N_raw.csv", index=False)
    opt_summ = summarize(opt, ["N", "metric"], ["optimal_lag_ratio", "peak_value"])
    opt_summ.to_csv(tables / "optimal_lag_by_N_summary.csv", index=False)
    return df, summ, opt, opt_summ


def run_controls(c: Config, tables: Path):
    rng_master = np.random.default_rng(c.seed + 1000)
    tau_ref = 1.0 / c.gamma
    specs = [
        ("main", "main", {}),
        ("equilibrium_control", "equilibrium", {}),
        ("memory_removal_gamma_fast", "main", {"gamma": c.gamma_fast}),
        ("slow_memory_gamma_slow", "main", {"gamma": c.gamma_slow}),
        ("no_feedback_beta0", "main", {"beta": 0.0}),
        ("no_hidden_coupling_kappa0", "main", {"kappa": 0.0}),
        ("ou_noise", "main", {"ou_noise": True}),
    ]

    rows = []
    for cond, kind, kwargs in specs:
        for run in range(c.n_runs):
            rng = np.random.default_rng(rng_master.integers(0, 2**32 - 1))
